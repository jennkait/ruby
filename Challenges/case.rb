weather_conditions= 'sunny'

case weather_conditions
when 'rainy'
  puts "Don't forget your umbrella!"
when 'cloudy'
  puts "You can leave the sunglasses at home."
when 'windy'
  puts "Make sure you wear your windbreaker!"
when 'sunny'
  puts "Lather up with sunscreen!"
else
  puts "Hmm, well I don't know what you should wear!"
end
